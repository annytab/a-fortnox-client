using System.Threading.Tasks;
using Microsoft.VisualStudio.TestTools.UnitTesting;
using Microsoft.Extensions.Logging;
using Annytab.Fortnox.Client.V3;

namespace TestProgram
{
    [TestClass]
    public class TestCustomers
    {
        #region Variables

        private Config config { get; set; }

        #endregion

        #region Constructors

        /// <summary>
        /// Create a new test instance
        /// </summary>
        public TestCustomers()
        {
            this.config = new Config();

        } // End of the constructor

        #endregion

        #region Methods

        /// <summary>
        /// Test to add a post
        /// </summary>
        [TestMethod]
        public async Task TestAddPost()
        {
            // Create a post
            CustomerRoot post = new CustomerRoot
            {
                Customer = new Customer
                {
                    CustomerNumber = "TEST-OLLE",
                    Name = "Testbolaget",
                    Email = "test@testbolaget.se",
                    DefaultTemplates = new DefaultTemplates
                    {
                        Invoice = "DEFAULTTEMPLATE"
                    }
                }
            };

            // Add the post
            FortnoxResponse<CustomerRoot> fr = await config.fortnox_client.Add<CustomerRoot>(post, "customers");

            // Log the error
            if (fr.model == null)
            {
                config.logger.LogError(fr.error);
            }

            // Test evaluation
            Assert.AreNotEqual(null, fr.model);

        } // End of the TestAddPost method

        /// <summary>
        /// Test to update a post
        /// </summary>
        [TestMethod]
        public async Task TestUpdatePost()
        {
            // Create a post
            CustomerRoot post = new CustomerRoot
            {
                Customer = new Customer
                {
                    CustomerNumber = "TEST-OLLE",
                    Name = "Testbolaget AAA",
                    Email = "test@testbolaget.se",
                    CountryCode = "NO",
                    Address1 = "Test 550"
                }
            };

            // Update the post
            FortnoxResponse<CustomerRoot> fr = await config.fortnox_client.Update<CustomerRoot>(post, "customers/TEST-OLLE");

            // Log the error
            if (fr.model == null)
            {
                config.logger.LogError(fr.error);
            }

            // Test evaluation
            Assert.AreNotEqual(null, fr.model);

        } // End of the TestUpdatePost method

        /// <summary>
        /// Test to get a post
        /// </summary>
        [TestMethod]
        public async Task TestGetPost()
        {
            // Get a post
            FortnoxResponse<CustomerRoot> fr = await config.fortnox_client.Get<CustomerRoot>("customers/1");

            // Log the error
            if (fr.model == null)
            {
                config.logger.LogError(fr.error);
            }

            // Test evaluation
            Assert.AreNotEqual(null, fr.model);

        } // End of the TestGetPost method

        /// <summary>
        /// Test to get a list
        /// </summary>
        [TestMethod]
        public async Task TestGetList()
        {
            // Get a list
            FortnoxResponse<CustomersRoot> fr = await config.fortnox_client.Get<CustomersRoot>("customers?email=test@testbolaget.se&limit=5&page=1");

            // Log the error
            if (fr.model == null)
            {
                config.logger.LogError(fr.error);
            }

            // Test evaluation
            Assert.AreNotEqual(null, fr.model);

        } // End of the TestGetList method

        /// <summary>
        /// Test to delete a post
        /// </summary>
        [TestMethod]
        public async Task TestDeletePost()
        {
            // Get a list
            FortnoxResponse<bool> fr = await config.fortnox_client.Delete("customers/TEST-OLLE");

            // Log the error
            if (fr.model == false)
            {
                config.logger.LogError(fr.error);
            }

            // Test evaluation
            Assert.AreEqual(true, fr.model);

        } // End of the TestDeletePost method

        #endregion

    } // End of the class

} // End of the namespace