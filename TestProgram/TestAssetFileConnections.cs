using System.Threading.Tasks;
using Microsoft.VisualStudio.TestTools.UnitTesting;
using Microsoft.Extensions.Logging;
using Annytab.Fortnox.Client.V3;

namespace TestProgram
{
    [TestClass]
    public class TestAssetFileConnections
    {
        #region Variables

        private Config config { get; set; }

        #endregion

        #region Constructors

        /// <summary>
        /// Create a new test instance
        /// </summary>
        public TestAssetFileConnections()
        {
            this.config = new Config();

        } // End of the constructor

        #endregion

        #region Methods

        /// <summary>
        /// Test to add a post
        /// </summary>
        [TestMethod]
        public async Task TestAddPost()
        {
            // Create a post
            AssetFileConnectionRoot post = new AssetFileConnectionRoot
            {
                AssetFileConnection = new AssetFileConnection
                {
                    FileId = "c8655c9c-db65-421a-bf93-39e706de3ddc",
                    AssetId = "1"
                }
            };

            // Add the post
            FortnoxResponse<AssetFileConnectionRoot> fr = await config.fortnox_client.Add<AssetFileConnectionRoot>(post, "assetfileconnections");

            // Log the error
            if (fr.model == null)
            {
                config.logger.LogError(fr.error);
            }

            // Test evaluation
            Assert.AreNotEqual(null, fr.model);

        } // End of the TestAddPost method

        /// <summary>
        /// Test to get a post
        /// </summary>
        [TestMethod]
        public async Task TestGetPost()
        {
            // Get a post
            FortnoxResponse<AssetFileConnectionRoot> fr = await config.fortnox_client.Get<AssetFileConnectionRoot>("assetfileconnections/c8655c9c-db65-421a-bf93-39e706de3ddc");

            // Log the error
            if (fr.model == null)
            {
                config.logger.LogError(fr.error);
            }

            // Test evaluation
            Assert.AreNotEqual(null, fr.model);

        } // End of the TestGetPost method

        /// <summary>
        /// Test to get a list
        /// </summary>
        [TestMethod]
        public async Task TestGetList()
        {
            // Get a list
            FortnoxResponse<AssetFileConnectionsRoot> fr = await config.fortnox_client.Get<AssetFileConnectionsRoot>("assetfileconnections?limit=2&page=1");

            // Log the error
            if (fr.model == null)
            {
                config.logger.LogError(fr.error);
            }

            // Test evaluation
            Assert.AreNotEqual(null, fr.model);

        } // End of the TestGetList method

        /// <summary>
        /// Test to delete a post
        /// </summary>
        [TestMethod]
        public async Task TestDeletePost()
        {
            // Delete a post
            FortnoxResponse<bool> fr = await config.fortnox_client.Delete("assetfileconnections/c8655c9c-db65-421a-bf93-39e706de3ddc");

            // Log the error
            if (fr.model == false)
            {
                config.logger.LogError(fr.error);
            }

            // Test evaluation
            Assert.AreEqual(true, fr.model);

        } // End of the TestDeletePost method

        #endregion

    } // End of the class

} // End of the namespace